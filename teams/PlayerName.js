function CreatePlayerDiv (id, object) {

    const container = document.getElementById(id);

    for (const key in object) {
    if (object.hasOwnProperty(key)) {
        const value = object[key];

        const playerDiv = document.createElement('div');
        playerDiv.classList.add('card', 'scrolling-team-players');

        const img = document.createElement('img');
        img.src = `https://resources.pulse.icc-cricket.com/players/210/${key}.png`;
        img.classList.add('scrolling-team-players-img');
        img.alt = value;

        const cardBody = document.createElement('div');
        cardBody.classList.add('card-body');

        const cardTitle = document.createElement('h5');
        cardTitle.classList.add('card-title', 'text-center');
        const playerLink = document.createElement('a');
        playerLink.href = `https://www.icc-cricket.com/rankings/mens/player-rankings/${key}`;
        playerLink.style.textDecoration = 'none';
        playerLink.target = '_blank';
        playerLink.innerHTML = value;
        
        cardTitle.appendChild(playerLink);
        cardBody.appendChild(cardTitle);
        playerDiv.appendChild(img);
        playerDiv.appendChild(cardBody);

        container.appendChild(playerDiv);
    }
    }

}

CreatePlayerDiv('Bangladesh', Bangladesh);